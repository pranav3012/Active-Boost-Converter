  /* USER CODE BEGIN 2 */
  HAL_ADCEx_Calibration_Start(&hadc1, ADC_SINGLE_ENDED);			// Start ADC Calibration Function call

  HAL_Delay(10);													// Small delay before all the initialization
  targetADC = targetVoltage * 94.3;									// 94.3 = 2500/26.5 counts / volt
  /* USER CODE END 2 */

