	  HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_SET);
	  unsigned int i = 0;											// local deceleration for multiple addition for averaging

	  uint32_t tempAvg = 0;											// local deceleration to temporarily store the averaging value

	  targetADC = targetVoltage * 94.1;

	  for (i = 0; i <= 9; i++)										// for loop for continuously  add the value 10 times.
	  {
		  startADC();												// Function call to start the ADC conversion

		  tempAvg = tempAvg + uhADCxConvertedValue;					// Adding the previous ADC converted value and the current ADC converted
	  }
	  // value
	uhADCxConvertedValue = tempAvg /10;								// Dividing the added value by 10 to get the average

	currentError = targetADC - uhADCxConvertedValue;				// Calculating the current error speed
	integral = integral + currentError;								// Calculating the integral error
	duty = (int32_t)((KP * currentError) + (KI * integral));		// Calculating PI values
	if (duty >= 400) duty = 400;									// Setting the maximum duty limit
	if (duty < 10) duty = 10;										// Setting the lowest duty limit
	__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, (duty));			// Updating the pulse for the duty cycle.
    /* USER CODE END WHILE */

